a = int(input("Enter a first number"))
b = int(input("Enter a second number"))
add = a + b
print(add)
subtract = a - b
print(subtract)
multiply = a * b
print(multiply)
divide = a / b
print(divide)

# modulus
if a%b == 0:
    print("the number is divisible without a remainder")
else:
    print(a%b, "with a remainder")
# exponent
exponent = a**b
print(exponent)

# floor_division
floor_division = a//b
print(floor_division)



