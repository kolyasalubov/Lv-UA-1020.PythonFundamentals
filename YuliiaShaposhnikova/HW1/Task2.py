a = int(input('Enter the value of a: '))
b = int(input('Enter the value of b: '))

sum_ab = a + b
diff_ab = a - b
multipl_ab = a * b
div_ab = a / b
expo_ab = a ** b
fdiv_ab = a // b
mod_ab = a % b

print(f'a + b = {sum_ab}')
print(f'a - b = {diff_ab}')
print(f'a * b = {multipl_ab}')
print(f'a / b = {div_ab}')
print(f'a ** b = {expo_ab}')
print(f'a // b = {fdiv_ab}')
print(f'a % b = {mod_ab}')
