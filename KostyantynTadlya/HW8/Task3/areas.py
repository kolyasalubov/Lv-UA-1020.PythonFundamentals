from math import pi
def area_of_triangle (h: float, a: float) -> float:
    """
    area_of_triangle calculates the area of triangele
    base on its heght and length of base side
    """
    return h*a/2

def area_of_rectangle (a: float, b: float) -> float:
    """
    area_of_rectangle calculates the area of rectangle
    """
    return a*b

def area_of_circle (r: float) -> float:
    """
    area_of_circle calculates the area of circle
    """
    return pi*r*r

